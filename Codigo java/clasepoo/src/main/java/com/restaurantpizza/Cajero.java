public class Cajero {

    private char name;

    private char lastName;

    private int cedula;
    
    public char getName() {
        return name;
    }

    public void setName(char name) {
        this.name = name;
    }

    public char getLastName() {
        return lastName;
    }

    public void setLastName(char lastName) {
        this.lastName = lastName;
    }

    public int getCedula() {
        return cedula;
    }

    public void setCedula(int cedula) {
        this.cedula = cedula;
    }
    
}
